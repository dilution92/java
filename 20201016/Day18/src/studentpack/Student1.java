package studentpack;

public class Student1 {
	String name;
	int number;

	public Student1() {
		
	}
	public Student1(String a, int b) {
		name =a;
		number =b;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	
}
