package kh.model.vo;

public class Kh {
	int khNo;
	String KhName;
	String addr;
	String fax;
	
	public int getKhNo() {
		return khNo;
	}
	public void setKhNo(int khNo) {
		this.khNo = khNo;
	}
	public String getKhName() {
		return KhName;
	}
	public void setKhName(String khName) {
		KhName = khName;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	
}
